    private void memberIdKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_memberIdKeyPressed
        char ch  = evt.getKeyChar();
        if(!((ch>='0' && ch<='9') || ch==KeyEvent.VK_BACK_SPACE   ))
        {
            evt.consume();
        }
        else
        {
            if(ch!=KeyEvent.VK_BACK_SPACE )
                str+= ch;
            else
            {
                if(str.length()!=0)
                {
                    str =  str.substring(0, str.length()-1);
                }
            }
            
            System.out.println(str);
            
            ar.clear();
            ar = new ConnectMSSQL().getMemberInfo("where MemberId =  '"+str+"'");
            if(ar.size()==0)
            {
                name.setText("MemberId Does Not Exist");
                due.setText("");
            }
            else
            {
                name.setText(ar.get(0).getName());
                due.setText(Integer.toString(ar.get(0).getDue()));
            }
            
        }
        
    }//GEN-LAST:event_memberIdKeyPressed

