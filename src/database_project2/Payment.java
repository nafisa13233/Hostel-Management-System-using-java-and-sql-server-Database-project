    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
        if(due.getText().length()==0)
        {
            JOptionPane.showMessageDialog(this, "Member Id is not valid","Error",JOptionPane.ERROR_MESSAGE);
        }
        else if(amount.getText().length()==0)
        {
            JOptionPane.showMessageDialog(this, "Amount is Empty","Error",JOptionPane.ERROR_MESSAGE);
        }
        else if(amount.getText().length()>7)
        {
            JOptionPane.showMessageDialog(this, "Amount size should be less than or equal to 7 digits","Error",JOptionPane.ERROR_MESSAGE);
        }
        else
        {
            PaymentInfo pay = new PaymentInfo();
            pay.setMemberId(memberId.getText());
            pay.setName(name.getText());
            pay.setAmount(Integer.parseInt(amount.getText()));
            SimpleDateFormat formatter = new SimpleDateFormat("yyyy/MM/dd HH:mm:ss");  
            Date date = new Date();  
            String f = formatter.format(date);  
            pay.setDateAndTime(f);
            pay.setPaymentId(f.substring(0,4)+f.substring(5,7)+f.substring(8,10)+f.substring(11,13)+f.substring(14,16)+f.substring(17,19)+new ConnectMSSQL().getSerial("PaymentSerial"));
            pay.setAdminId(this.AdminId);
            System.out.println(pay.getPaymentId());
            try
            {
                new ConnectMSSQL().addPayment(pay);
                
                new ConnectMSSQL().update("SerialNo", "PaymentSerial = PaymentSerial+1");
                ArrayList<MemberInfo> arr = new ConnectMSSQL().getMemberInfo("where MemberId = "+memberId.getText());
                //System.out.println(arr.size());
                MemberInfo user = arr.get(0);
                user.setDue(user.getDue()-pay.getAmount());
                new ConnectMSSQL().updateMember(user);
                JOptionPane.showMessageDialog(this, "Payment Successful","Succesful",JOptionPane.INFORMATION_MESSAGE);
                resetButtonActionPerformed(evt);
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(this, "Error Occured,Payment Unsuccessful","Error",JOptionPane.ERROR_MESSAGE);
            }
        }
            
    }//GEN-LAST:event_submitButtonActionPerformed

